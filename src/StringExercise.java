public class StringExercise {
    public static void main(String[] args) {
        String stringBasics = "We don't need no education\n" +
                "We don't need no thought control\n";
        System.out.println(stringBasics);
        stringBasics = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(stringBasics);
        stringBasics = "In windows, the main drive is usually C:\\";
        System.out.println(stringBasics);
        stringBasics = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!\n";
        System.out.println(stringBasics);



    }
}
